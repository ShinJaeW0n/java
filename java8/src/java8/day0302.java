package java8;

public class day0302 {

	public static void main(String[] args) {
		/* 기본자료형은 각각을 정의하는 클래스가 존재합니다.
		 * byte -> Byte 
		 * short -> Short
		 * int -> Integer
		 * long -> Long
		 * float -> Float
		 * double -> Double
		 * char -> Character
		 * 
		 * 각각의 클래스에는 상수로 MAX_VALUE(최대값)와 MIN_VALUE(최소값)가 존재합니다.
		 */
		
		System.out.println("int형의 최대값:" + Integer.MAX_VALUE);
		System.out.println("int형의 최소값:" + Integer.MIN_VALUE);
		System.out.println("byte형의 최대값:" + Byte.MAX_VALUE);
		System.out.println("byte형의 최소값:" + Byte.MIN_VALUE);
		
	}
}
