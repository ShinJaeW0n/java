package Day18;

public class Func2Ex {
			public static void main(String[] args) {
				Func2 func1 = new Func2() { // <-익명구현객체
					
					@Override
					public int method(int x, int y) {
						int result = x+y;
						return result;
					}
				};
				
				System.out.println(func1.method(10, 20)); // <-익명구현객체
			
					
				//익명구현객체 중에 메소드가 1개인것은 람다식으로 나타낼 수 있다.
				
				Func2 func2 = (int x, int y) -> {
					int result = x+y;
					return result;
				};
				System.out.println(func2.method(10, 20));
				
							//2줄이상일 경우 중괄호 반드시 있어야한다.{}
				Func2 func3 = ( x,  y ) -> { return x+y; };
				System.out.println(func2.method(10, 20));
				
										//1줄일경우 {}삭제 가능. return값도 삭제 가능
				Func2 func4 = ( x,  y ) ->  x + y;
				System.out.println(func2.method(10, 20));
			}
}
