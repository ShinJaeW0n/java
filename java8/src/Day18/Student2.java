public class Student2 {
			
			private String name;
			private String sex;
			private int score;
			
			
			//source에서 생성자 가져오기.
			public Student2(String name, String sex, int score) {
				this.name = name;
				this.sex = sex;
				this.score = score;
			}


			public String getName() {
				return name;
			}


			public String getSex() {
				return sex;
			}


			public int getScore() {
				return score;
			}
			
			
	
	
}
